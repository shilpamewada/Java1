/*Single Dimensional Array: It's like a single row of boxes, where each box holds one item. 
You access each item by its position in the row (index).
It's declared with a single pair of square brackets []. Accessing elements involves using a single index.

Two Dimensional Array: It's like a grid or a table, where you have rows and columns of boxes. 
You access each item by its position in both the row and the column.
It's declared with two pairs of square brackets [][]. 
Accessing elements involves using two indices: one for the row and one for the column. */