package Ifelse;
//Demo on Switch Case
public class SwitchCase {

	public static void main(String[] args) {
		
		String ch = "y";
		
		switch(ch){
		case "A" :
		case "E" :
		case "I" :
		case "O" :
		case "U" :
		case "a" :
		case "e" :
		case "i" :
		case "o" :
		case "u" : System.out.println("Vowel");
		break ;
		
		default: System.out.println("Consonent");
		break;
		
		}
		
	}

}
